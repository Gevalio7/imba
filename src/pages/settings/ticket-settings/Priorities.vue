// –ó–∞–≥—Ä—É–∑–∫–∞ –¥–∞–Ω–Ω—ã—Ö –∏–∑ API
const fetchPriorities = async () => {
  try {
    loading.value = true
    error.value = null
    const { data } = await get('/priorities')
    priorities.value = data.value as Priority[]
  } catch (err) {
    error.value = '–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç–æ–≤'
