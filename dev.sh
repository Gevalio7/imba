        print_color $GREEN "‚úÖ –ë—ç–∫–µ–Ω–¥ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω"
    fi
    
    # –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è –æ—á–∏—Å—Ç–∫–∞ –ø–æ—Ä—Ç–æ–≤
    kill_port 5173
    kill_port 5174
    kill_port 5175
    kill_port 5176
    kill_port 3000
    
    print_color $CYAN "üëã –î–æ —Å–≤–∏–¥–∞–Ω–∏—è!"
    exit 0
}

